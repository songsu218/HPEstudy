// 산술연산자
// +
// -
// *
// /
// % (나머지값)
// ** (거듭제곱)

console.log(5 + 2);
console.log(5 - 2);
console.log(5 / 2);
console.log(5 * 2);
console.log(5 % 2);
console.log(5 ** 2);
console.log(Math.pow(5, 2));

let text1 = '두개의 ' + '문자를 연결';
let text2 = `두개의 문자를 연결`;
console.log(text1);
console.log(text2);

let text3 = '1' + 1;
console.log(text3);
let text4 = 1 + '1';
console.log(text4);

let text5 = '1' * 1;
console.log(text5);
let text6 = 1 * '1';
console.log(text6);
